let lang = 'en';
function detect() {
    const s = localStorage.getItem('qo_lang');
    if (s && T[s]) return s;
    const n = (navigator.language || 'en').substring(0, 2).toLowerCase();
    return T[n] ? n : 'en';
}
export function initI18n() { lang = detect(); apply(); return lang; }
export function setLanguage(l) { if (T[l]) { lang = l; localStorage.setItem('qo_lang', l); apply(); } }
export function t(key, ...args) {
    let s = (T[lang] && T[lang][key]) || T.en[key] || key;
    args.forEach((a, i) => { s = s.replace(`{${i}}`, a); });
    return s;
}
export function getCurrentLang() { return lang; }
function apply() {
    document.querySelectorAll('[data-i18n]').forEach(el => { el.textContent = t(el.getAttribute('data-i18n')); });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => { el.placeholder = t(el.getAttribute('data-i18n-placeholder')); });
}
